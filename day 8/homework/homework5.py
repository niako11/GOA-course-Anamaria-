#5) დაწერეთ 
#პროგრამა, რომელიც გამოითვლის 5 რიცხვის საშუალო არითმეტიკულს, მიღებული რიცხვი შეინახეთ ცვლადში, აიყვანეთ კუბში(მესამე ხარისხში) და დაბეჭდეთ.
num1=10
num2=20
num3=30
num4=40
num5=50
#საშუალო არითმეტიკულის განსაზღვრა.
average=(num1 + num2 + num3 + num4 + num5) / 5
#კუბში აყვანა
cubed_result=average * 3
#დაბეჭვდა
print(average)
print(cubed_result)
